		AREA RESET,DATA,READONLY
		EXPORT __Vectors
__Vectors
		DCD 0x10001000
		DCD Reset_Handler
		ALIGN
		AREA mycode,CODE,READONLY
		ENTRY
		EXPORT Reset_Handler
Reset_Handler
		LDR R0,=NUM
		LDR R1,[R0]
		LDR R2,=DST
		MOV R3,#-1
UP		LSRS R1,#1
		ADC R3,#0
		SUB R1,#1
		CMP R1,#0
		BNE UP
		STR R3,[R2]
STOP	B STOP
NUM DCD 256
		AREA mydata,DATA,READWRITE
DST DCD 0
		END